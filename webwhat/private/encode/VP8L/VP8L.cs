namespace VP8L {
  using System;
  using System.Collections.Generic;
  using System.Drawing;
  using System.IO;
  using System.Linq;

  public static class Program {
    public static void Main(string[] args) {
      var inputPath = args.Length >= 1 ? args[0] : "input.png";
      var outputPath = args.Length >= 2 ? args[1] : "output.webp";
      var bitmap = new Bitmap(inputPath);
      var image = new Image(bitmap);
      using(var w = new BinaryWriter(File.Create(outputPath))) {
        Format.Encode(w, image);
      }
    }
  }
}
